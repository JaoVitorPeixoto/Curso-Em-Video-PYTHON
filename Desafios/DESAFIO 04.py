v = input('Informe qualquer coisa:')
print('O tipo primitivo do valor é:', type(v))
print('só tem espaços?', v.isspace())
print('só tem letra maiúscula? ', v.isupper())
print('só tem letra minúscula? ', v.islower())
print('é um número?', v.isnumeric())
print('só tem letras?', v.isalpha())
print('ele é alfanúmerico?', v.isalnum())
print('está capitalizada?', v.istitle())
print('é decimal?', v.isdecimal())


