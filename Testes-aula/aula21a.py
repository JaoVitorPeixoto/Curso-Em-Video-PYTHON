def contador(i, f, p):
    """
    ->  Faz uma contagem e mostra na tela.
    :param i: Inicío da contagem
    :param f: Fim da contagem
    :param p: Passo da contagem
    :return: Sem retorno
    <<<< FUNÇÃO CRIADA POR JOÃO VITOR DO IFBA >>>>
    """
    for c in range(i, f+1, p):
        print(c, end=' ')


