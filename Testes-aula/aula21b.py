def somar(a=0, b=0, c=0):
    """
    -> Faz a soma de no máximo 3 valores.
    :param a: Primeiro valor
    :param b: Segundo valor
    :param c: Terceiro valor
    :return: sem retorno
    Criado por João Vitor do IFBA.
    """
    s = a + b + c
    print(f'A soma é {s}')


